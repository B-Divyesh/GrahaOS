// Linear-scan codepoint→glyph lookup. 11 entries; constant-time enough for
// a render path that processes each cell once per frame.
static const uint8_t *find_box_glyph(uint32_t cp) {
    for (uint32_t i = 0; i < box_glyphs_count; i++) {
        if (box_glyphs[i].codepoint == cp) return box_glyphs[i].bitmap;
    }
    return NULL;
}

// --- Helper Functions ---
static size_t strlen(const char *str) {
    if (!str) return 0;
    size_t len = 0;
    while (str[len]) len++;
    return len;
}

static bool is_valid_coordinates(uint32_t x, uint32_t y) {
    return (x < fb_width && y < fb_height);
}

// Internal drawing functions (no locking)
static void _draw_pixel_unsafe(uint32_t x, uint32_t y, uint32_t color) {
    if (is_valid_coordinates(x, y)) {
        fb_addr[y * (fb_pitch / 4) + x] = color;
    }
}

static void _draw_rect_unsafe(uint32_t x, uint32_t y, uint32_t width, uint32_t height, uint32_t color) {
    uint32_t x_end = (x + width < fb_width) ? x + width : fb_width;
    uint32_t y_end = (y + height < fb_height) ? y + height : fb_height;
    
    for (uint32_t j = y; j < y_end; j++) {
        for (uint32_t i = x; i < x_end; i++) {
            fb_addr[j * (fb_pitch / 4) + i] = color;
        }
    }
}

static void _draw_char_unsafe(char c, uint32_t x, uint32_t y, uint32_t fg_color) {
    if (c < 32 || c > 126) return;

    const uint8_t *glyph = font_8x16[c - 32];

    for (int row = 0; row < 16; row++) {
        for (int col = 0; col < 8; col++) {
            if (glyph[row] & (0x80 >> col)) {
                _draw_pixel_unsafe(x + col, y + row, fg_color);
            }
        }
    }
}

// Check if we're in interrupt context
static bool in_interrupt_context(void) {
    // Check if interrupts are disabled - likely in interrupt handler
    uint64_t flags;
    asm volatile("pushfq; pop %0" : "=r"(flags));
    return !(flags & 0x200);  // IF flag
}

// Non-locking versions for use in interrupt handlers
void framebuffer_draw_string_nolock(const char *str, uint32_t x, uint32_t y, 
                                    uint32_t fg_color, uint32_t bg_color) {
    if (!str || !fb_addr) return;
    
    size_t len = strlen(str);
    if (len == 0) return;
    
    // Draw directly without any locking
    // This is safe only if called from interrupt context where nothing else can run
    _draw_rect_unsafe(x, y, len * 8, 16, bg_color);
    
    for (size_t i = 0; i < len; i++) {
        _draw_char_unsafe(str[i], x + (i * 8), y, fg_color);
    }
    
    // Memory barrier to ensure writes complete
    asm volatile("mfence" ::: "memory");
}


// Driver framework stats callback
static int fb_get_driver_stats(state_driver_stat_t *stats, int max) {
    if (!stats || max < 3) return 0;
    const char *k0 = "width";
    for (int i = 0; k0[i] && i < STATE_STAT_KEY_LEN - 1; i++) stats[0].key[i] = k0[i];
    stats[0].key[STATE_STAT_KEY_LEN - 1] = '\0';
    stats[0].value = (uint64_t)fb_width;
    const char *k1 = "height";
    for (int i = 0; k1[i] && i < STATE_STAT_KEY_LEN - 1; i++) stats[1].key[i] = k1[i];
    stats[1].key[STATE_STAT_KEY_LEN - 1] = '\0';
    stats[1].value = (uint64_t)fb_height;
    const char *k2 = "pitch";
    for (int i = 0; k2[i] && i < STATE_STAT_KEY_LEN - 1; i++) stats[2].key[i] = k2[i];
    stats[2].key[STATE_STAT_KEY_LEN - 1] = '\0';
    stats[2].value = (uint64_t)fb_pitch;
    return 3;
}

// Public functions
bool framebuffer_init(volatile struct limine_framebuffer_request *fb_request) {
    if (!fb_request || !fb_request->response ||
        fb_request->response->framebuffer_count < 1) {
        return false;
    }

    fb = fb_request->response->framebuffers[0];
    fb_addr = (uint32_t *)fb->address;
    fb_pitch = fb->pitch;
    fb_width = fb->width;
    fb_height = fb->height;
    
    // Reinitialize the lock (even though it's statically initialized)
    spinlock_init(&fb_lock, "framebuffer");

    // Phase 14: CAN registration moved to framebuffer_register_cap()
    // because can_entry_t now lives in a slab that isn't ready at this
    // point in boot. Main.c calls framebuffer_register_cap after slab
    // init + hw cap registration.

    return true;
}

void framebuffer_register_cap(void) {
    static int fb_registered = 0;
    if (fb_registered) return;
    // Register with Capability Activation Network
    const char *fb_deps[] = {"framebuffer_hw"};
    cap_op_t fb_ops[4];
    cap_op_set(&fb_ops[0], "draw_rect",   5, 1);
    cap_op_set(&fb_ops[1], "draw_string", 5, 1);
    cap_op_set(&fb_ops[2], "draw_char",   4, 1);
    cap_op_set(&fb_ops[3], "clear",       1, 1);
    cap_register("display", CAP_DRIVER, CAP_SUBTYPE_DISPLAY, -1, fb_deps, 1,
                 fb_activate, fb_deactivate, fb_ops, 4, fb_get_driver_stats);
    fb_registered = 1;
}

// Phase 16: CAN activate — mark the display live. On the very first activation
// (boot path) we skip the memset+banner so the early boot banner painted by
// framebuffer_init remains visible and the boot-time cap_activate loop doesn't
// burn 3 MB of MMIO writes per pass. Subsequent reactivations (post-deactivate)
// do perform the clear+banner so the operator sees the transition.
int fb_activate(void) {
    static bool s_activated_before = false;
    g_fb_active = true;
    if (s_activated_before && fb_addr) {
        spinlock_acquire(&fb_lock);
        for (uint32_t i = 0; i < fb_width * fb_height; i++) {
            fb_addr[i] = 0x00000000;
        }
        spinlock_release(&fb_lock);
        framebuffer_draw_string("GrahaOS reactivated",
                                 10, 10, COLOR_GREEN, 0x00000000);
    }
    s_activated_before = true;
    return 0;
}

// Phase 16: CAN deactivate — memset the framebuffer to 0x00 so the screen
// goes black, then disable the active-flag so subsequent draws are no-ops.
// The Limine framebuffer mapping stays; Phase 17 may unmap it.
int fb_deactivate(void) {
    if (fb_addr) {
        spinlock_acquire(&fb_lock);
        for (uint32_t i = 0; i < fb_width * fb_height; i++) {
            fb_addr[i] = 0x00000000;
        }
        spinlock_release(&fb_lock);
    }
    g_fb_active = false;
    return 0;
}

bool framebuffer_is_active(void) { return g_fb_active; }
uint32_t framebuffer_read_pixel(uint32_t x, uint32_t y) {
    if (!fb_addr || x >= fb_width || y >= fb_height) return 0;
    return fb_addr[y * (fb_pitch / 4) + x];
}


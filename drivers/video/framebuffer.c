// Phase 27 Stage A4: force-draw a single 8x16 cell into FB regardless of
// g_fbd_alive / g_fb_active. Used by:
//   (1) console_render_synthetic_frame() for the gate test fbd_render
//   (2) panic.c/oops emit path before/after kpanic claims the screen
// Codepoints outside the basic ASCII range are rendered as a space (blank
// 8x16 block at bg_color) — extended glyphs land in Stage A5 with libtui's
// font extension.
void framebuffer_force_draw_cell(uint32_t pixel_x, uint32_t pixel_y,
                                 uint32_t codepoint,
                                 uint32_t fg_color, uint32_t bg_color) {
    if (!fb_addr) return;
    if (pixel_x + 8 > fb_width || pixel_y + 16 > fb_height) return;

    bool in_intr = in_interrupt_context();
    if (!in_intr) spinlock_acquire(&fb_lock);

    // Background fill (always paint so cells that toggle from glyph-set to
    // glyph-clear don't leak the previous frame's pixels).
    for (uint32_t row = 0; row < 16; row++) {
        for (uint32_t col = 0; col < 8; col++) {
            fb_addr[(pixel_y + row) * (fb_pitch / 4) + (pixel_x + col)] = bg_color;
        }
    }

    // Foreground glyph. Stage A5 supports:
    //   ASCII 0x20..0x7E    — font_8x16 array
    //   Unicode 0x2500..2518 subset — find_box_glyph() table
    //   Sprite cells 0xE100..0xE7FF — Stage B1 (rendered as blanks here)
    // Phase 29 Session E — extended Unicode coverage.  Each range maps to
    // find_box_glyph (a linear-scan table) for glyphs we explicitly defined;
    // unmapped codepoints fall back to a blank cell (the bg fill above
    // already handled).
    const uint8_t *glyph = NULL;
    if (codepoint >= 32 && codepoint <= 126) {
        glyph = font_8x16[codepoint - 32];
    } else if (codepoint >= 0xA0 && codepoint <= 0xFF) {
        // Phase 29 FU29.X.font_cjk — Latin-1 Supplement (always non-NULL
        // within range; unauthored slots are intentional blanks).
        glyph = font_latin1[codepoint - 0xA0];
    } else if (codepoint >= 0x2190 && codepoint <= 0x21FF) {
        // Arrows.
        glyph = find_box_glyph(codepoint);
    } else if (codepoint >= 0x2500 && codepoint <= 0x257F) {
        // Box drawing (already supported in box_glyphs).
        glyph = find_box_glyph(codepoint);
    } else if (codepoint >= 0x2580 && codepoint <= 0x259F) {
        // Block elements (extra entries added in Session E).
        glyph = find_box_glyph(codepoint);
    } else if (codepoint >= 0x25A0 && codepoint <= 0x25FF) {
        // Geometric shapes.
        glyph = find_box_glyph(codepoint);
    } else if (codepoint == 0x2022 || codepoint == 0x2026) {
        glyph = find_box_glyph(codepoint);
    } else if (codepoint >= 0x2600 && codepoint <= 0x27BF) {
        // Misc symbols + dingbats (smileys, suits, music).
        glyph = find_box_glyph(codepoint);
    }
    // Phase 29 FU29.X.font_cjk — visible tofu fallback for any printable
    // codepoint we have no glyph for (e.g. unmapped CJK, or an in-range box
    // codepoint absent from the table). Exclude: control ranges (<0x20 and
    // C1 0x80..0x9F), space, and the sprite PUA (0xE100..0xE7FF, dispatched
    // upstream in console_render_synthetic_frame — guarded here as
    // belt-and-suspenders; framebuffer.c can't see TUI_SPRITE_BASE/END).
    if (!glyph && codepoint >= 0x20 && codepoint != ' ' &&
        !(codepoint >= 0x80 && codepoint <= 0x9F) &&
        !(codepoint >= 0xE100u && codepoint <= 0xE7FFu)) {
        glyph = glyph_missing;
    }
    if (glyph) {
        for (int row = 0; row < 16; row++) {
            uint8_t bits = glyph[row];
            if (!bits) continue;
            for (int col = 0; col < 8; col++) {
                if (bits & (0x80 >> col)) {
                    fb_addr[(pixel_y + row) * (fb_pitch / 4) + (pixel_x + col)] = fg_color;
                }
            }
        }
    }

    asm volatile("mfence" ::: "memory");
    if (!in_intr) spinlock_release(&fb_lock);
}

// Phase 27 Stage B1: render a sprite (8x16 1bpp) at the given pixel position.
void framebuffer_force_draw_sprite(uint32_t pixel_x, uint32_t pixel_y,
                                   const uint8_t glyph[16],
                                   uint32_t fg_color, uint32_t bg_color) {
    if (!fb_addr || !glyph) return;
    if (pixel_x + 8 > fb_width || pixel_y + 16 > fb_height) return;

    bool in_intr = in_interrupt_context();
    if (!in_intr) spinlock_acquire(&fb_lock);

    for (uint32_t row = 0; row < 16; row++) {
        uint8_t bits = glyph[row];
        for (uint32_t col = 0; col < 8; col++) {
            uint32_t color = (bits & (0x80u >> col)) ? fg_color : bg_color;
            fb_addr[(pixel_y + row) * (fb_pitch / 4) + (pixel_x + col)] = color;
        }
    }
    asm volatile("mfence" ::: "memory");
    if (!in_intr) spinlock_release(&fb_lock);
}

// Phase 27 Stage B1 + FU27.X.alpha_blend: write one pixel from the overlay
// buffer to the FB with proper RGBA alpha blending. Source pixel format is
// 0xAARRGGBB. Fast paths for fully opaque (alpha=0xFF) and fully
// transparent (alpha=0x00). Otherwise performs the standard 8-bit alpha
// blend out = (src*src_a + dst*(255-src_a) + 128) / 255 per channel; the
// "+128 / 255" form rounds to nearest without dividing twice and keeps the
// math fully integer (no division per output channel beyond a constant
// /255 the compiler folds into a multiply). No precompute table is needed
// for the line rates this function sees (typical overlay damage rect is
// O(thousands) pixels per frame, not O(millions)).
void framebuffer_force_blit_pixel(uint32_t pixel_x, uint32_t pixel_y, uint32_t argb) {
    if (!fb_addr) return;
    if (pixel_x >= fb_width || pixel_y >= fb_height) return;
    uint8_t src_a = (uint8_t)((argb >> 24) & 0xFFu);
    uint32_t *slot = &fb_addr[pixel_y * (fb_pitch / 4) + pixel_x];
    if (src_a == 0xFF) {
        // Fully opaque source: skip the read-modify-write.
        *slot = argb & 0x00FFFFFFu;
        return;
    }
    if (src_a == 0) {
        // Fully transparent source: leave dst pixel unchanged.
        return;
    }
    uint32_t dst = *slot;
    uint8_t dst_r = (uint8_t)((dst >> 16) & 0xFFu);
    uint8_t dst_g = (uint8_t)((dst >>  8) & 0xFFu);
    uint8_t dst_b = (uint8_t)((dst      ) & 0xFFu);
    uint8_t src_r = (uint8_t)((argb >> 16) & 0xFFu);
    uint8_t src_g = (uint8_t)((argb >>  8) & 0xFFu);
    uint8_t src_b = (uint8_t)((argb      ) & 0xFFu);
    uint32_t inv_a = 255u - src_a;
    uint8_t out_r = (uint8_t)(((uint32_t)src_r * src_a + (uint32_t)dst_r * inv_a + 128u) / 255u);
    uint8_t out_g = (uint8_t)(((uint32_t)src_g * src_a + (uint32_t)dst_g * inv_a + 128u) / 255u);
    uint8_t out_b = (uint8_t)(((uint32_t)src_b * src_a + (uint32_t)dst_b * inv_a + 128u) / 255u);
    *slot = ((uint32_t)out_r << 16) | ((uint32_t)out_g << 8) | (uint32_t)out_b;
}

uint32_t framebuffer_get_width(void) {
    return fb_width;
}

uint32_t framebuffer_get_height(void) {
    return fb_height;
}

void framebuffer_draw_pixel(uint32_t x, uint32_t y, uint32_t color) {
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    spinlock_acquire(&fb_lock);
    _draw_pixel_unsafe(x, y, color);
    spinlock_release(&fb_lock);
}

void framebuffer_draw_rect(uint32_t x, uint32_t y, uint32_t width, uint32_t height, uint32_t color) {
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    if (x >= fb_width || y >= fb_height || width == 0 || height == 0) {
        return;
    }
    
    // Check if in interrupt context
    if (in_interrupt_context()) {
        // Draw without lock
        _draw_rect_unsafe(x, y, width, height, color);
        return;
    }
    
    // Normal path with lock
    spinlock_acquire(&fb_lock);
    _draw_rect_unsafe(x, y, width, height, color);
    spinlock_release(&fb_lock);
}

void framebuffer_draw_rect_outline(uint32_t x, uint32_t y, uint32_t width, uint32_t height, uint32_t color) {
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    if (x >= fb_width || y >= fb_height || width == 0 || height == 0) {
        return;
    }
    
    spinlock_acquire(&fb_lock);
    
    // Top and bottom edges
    for (uint32_t i = x; i < x + width && i < fb_width; i++) {
        _draw_pixel_unsafe(i, y, color);
        if (y + height - 1 < fb_height) {
            _draw_pixel_unsafe(i, y + height - 1, color);
        }
    }

    // Left and right edges
    for (uint32_t j = y + 1; j < y + height - 1 && j < fb_height; j++) {
        _draw_pixel_unsafe(x, j, color);
        if (x + width - 1 < fb_width) {
            _draw_pixel_unsafe(x + width - 1, j, color);
        }
    }
    
    spinlock_release(&fb_lock);
}

void framebuffer_draw_char(char c, uint32_t x, uint32_t y, uint32_t fg_color) {
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    spinlock_acquire(&fb_lock);
    _draw_char_unsafe(c, x, y, fg_color);
    spinlock_release(&fb_lock);
}

// Modified main framebuffer_draw_string function
void framebuffer_draw_string(const char *str, uint32_t x, uint32_t y,
                             uint32_t fg_color, uint32_t bg_color) {
    if (!str || !fb_addr) return;
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    
    // Check if we're in interrupt context
    if (in_interrupt_context()) {
        // In interrupt - use non-locking version
        framebuffer_draw_string_nolock(str, x, y, fg_color, bg_color);
        return;
    }
    
    // Normal context - use locking
    spinlock_acquire(&fb_lock);
    
    size_t len = strlen(str);
    if (len == 0) {
        spinlock_release(&fb_lock);
        return;
    }
    
    _draw_rect_unsafe(x, y, len * 8, 16, bg_color);
    
    for (size_t i = 0; i < len; i++) {
        _draw_char_unsafe(str[i], x + (i * 8), y, fg_color);
    }
    
    asm volatile("mfence" ::: "memory");
    
    spinlock_release(&fb_lock);
}


void framebuffer_draw_hex(uint64_t value, int x, int y, uint32_t fg_color, uint32_t bg_color) {
    char buffer[19];
    const char *hex_chars = "0123456789ABCDEF";
    
    buffer[0] = '0';
    buffer[1] = 'x';
    
    for (int i = 0; i < 16; i++) {
        uint8_t nibble = (value >> (60 - i * 4)) & 0xF;
        buffer[2 + i] = hex_chars[nibble];
    }
    buffer[18] = '\0';
    
    framebuffer_draw_string(buffer, x, y, fg_color, bg_color);
}

// Clear function with interrupt safety
void framebuffer_clear(uint32_t color) {
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    if (in_interrupt_context()) {
        // Clear without lock
        for (uint32_t i = 0; i < fb_width * fb_height; i++) {
            fb_addr[i] = color;
        }
        return;
    }
    
    spinlock_acquire(&fb_lock);
    for (uint32_t i = 0; i < fb_width * fb_height; i++) {
        fb_addr[i] = color;
    }
    spinlock_release(&fb_lock);
}

void framebuffer_draw_rsp_error(uint64_t code) {
    framebuffer_draw_rect(300, 300, 400, 100, COLOR_RED);
    framebuffer_draw_string("ZERO RSP DETECTED!", 320, 320, COLOR_WHITE, COLOR_RED);
    framebuffer_draw_hex(code, 320, 340, COLOR_WHITE, COLOR_RED);
}

// Thread-safe string drawing that doesn't panic on lock issues
void framebuffer_draw_string_safe(const char *str, uint32_t x, uint32_t y,
                                  uint32_t fg_color, uint32_t bg_color) {
    if (!str || !fb_addr) return;
    if (!g_fb_active) return;
    if (fb_should_bypass()) return;
    
    // Try to acquire lock with timeout
    int attempts = 1000000;
    while (fb_lock.locked && attempts-- > 0) {
        asm volatile("pause");
    }
    
    if (attempts <= 0) {
        // Couldn't get lock - draw without lock (risky but won't crash)
        // This is only for initialization messages
        size_t len = strlen(str);
        if (len == 0) return;
        
        // Draw directly without lock
        for (size_t i = 0; i < len; i++) {
            _draw_char_unsafe(str[i], x + (i * 8), y, fg_color);
        }
        return;
    }
    
    // Got the lock - use normal path
    spinlock_acquire(&fb_lock);
    
    size_t len = strlen(str);
    if (len == 0) {
        spinlock_release(&fb_lock);
        return;
    }
    
    // Draw background
    _draw_rect_unsafe(x, y, len * 8, 16, bg_color);
    
    // Draw characters
    for (size_t i = 0; i < len; i++) {
        _draw_char_unsafe(str[i], x + (i * 8), y, fg_color);
    }
    
    asm volatile("mfence" ::: "memory");
    
    spinlock_release(&fb_lock);
}
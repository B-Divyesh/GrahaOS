// kernel/fs/grahafs_v2.h
//
// Phase 19 — GrahaFS v2.
//
// Complete on-disk rewrite over v1. The headline invariants:
//
//   * 512-byte inodes with WIRED indirect + double-indirect pointers.
//     Total addressable capacity per file: 12 direct + 1024 indirect +
//     1024*1024 double-indirect blocks = 4 GB + 48 KB.
//
//   * 64 MB (16384-block) contiguous journal region. Every mutation —
//     data bytes, inode updates, segment metadata, superblock advances —
//     lands in the journal first. Two-barrier commit protocol (see
//     journal.h) ensures crash consistency on real AHCI devices.
//
//   * 128 MB (32768-block) logical segments. Each segment has a header
//     at block 0 carrying magic, refcount, state, and version-record
//     bookkeeping. Version records live in segments so GC can reclaim
//     a whole segment at once when refcount hits zero (SEALED only).
//
//   * Per-file version chains (up to 16 retained). One version_record
//     per close(). Revert is non-destructive (new record with
//     parent_version set). Phase 24 (COW snapshots) and Phase 25
//     (speculative execution) build on this substrate.
//
//   * SimHash computed synchronously at close; cluster assignment runs
//     asynchronously via the per-mount recluster work queue (NOT a
//     stream_t — see Phase 19 plan Tricky Bit #5).
//
// Layout drift is caught by _Static_assert on every on-disk size.
// Magic numbers are verified at every read; mismatch => -EBADFS at
// userspace boundary (never a silent fallthrough).
#pragma once

#include <stdbool.h>
#include <stddef.h>
#include <stdint.h>

#include "../sync/spinlock.h"

// ---------------------------------------------------------------------------
// Magic numbers. All verified on every disk read.
// ---------------------------------------------------------------------------
#define GRAHAFS_V2_SB_MAGIC         0x47524148414F5322ULL  // "GRAHAOS\""
                                                           // v1 was 0x...21.
#define GRAHAFS_V2_INODE_MAGIC      0x1203A1FEu            // "v2 inode"
#define GRAHAFS_V2_SEGMENT_MAGIC    0xC0FFEE52u            // "coffee 52"
#define GRAHAFS_V2_VERSION_MAGIC    0x7E5E12DBu            // "version"
#define GRAHAFS_V2_JOURNAL_BEGIN    0xCAFE7193u            // "journal txn"
#define GRAHAFS_V2_JOURNAL_COMMIT   0xCAFECAFEu            // "journal commit"
#define GRAHAFS_V2_INODE_CACHE_MAGIC 0xCAFE1923u           // In-memory only.

// v1 magic (for compat detection at mount).
#define GRAHAFS_V1_SB_MAGIC         0x47524148414F5321ULL  // "GRAHAOS!"

// ---------------------------------------------------------------------------
// Geometry.
// ---------------------------------------------------------------------------
#define GRAHAFS_V2_BLOCK_SIZE       4096u
#define GRAHAFS_V2_BLOCK_SHIFT      12u
#define GRAHAFS_V2_INODE_SIZE       512u
#define GRAHAFS_V2_INODES_PER_BLOCK (GRAHAFS_V2_BLOCK_SIZE / GRAHAFS_V2_INODE_SIZE)

#define GRAHAFS_V2_SEGMENT_BLOCKS   32768u  // 128 MB per segment.
#define GRAHAFS_V2_JOURNAL_BLOCKS   16384u  // 64 MB journal.
#define GRAHAFS_V2_INODE_COUNT_MAX  16384u  // 4x v1.

#define GRAHAFS_V2_DIRECT_BLOCKS    12u
#define GRAHAFS_V2_INDIRECT_PTRS    (GRAHAFS_V2_BLOCK_SIZE / sizeof(uint32_t))  // 1024.
#define GRAHAFS_V2_MAX_VERSIONS     16u

// Inode types (match v1 so vfs.h constants keep working).
#define GRAHAFS_V2_TYPE_FILE        1u
#define GRAHAFS_V2_TYPE_DIRECTORY   2u

// Segment states.
#define GRAHAFS_V2_SEG_FREE         0u
#define GRAHAFS_V2_SEG_ACTIVE       1u   // Writers may still allocate here.
#define GRAHAFS_V2_SEG_SEALED       2u   // Closed to new writes.
#define GRAHAFS_V2_SEG_RECLAIMABLE  3u   // SEALED + refcount==0; GC frees.

// Version record flags. COMPRESSED / ENCRYPTED are reserved (no in-tree
// codec yet); REVERT_CREATED is set by SYS_FS_REVERT to mark a version
// that was generated by an explicit revert operation rather than a normal
// write — useful for distinguishing "user wrote this" vs "user reverted
// to a prior state" in the version history.
#define VE_FLAG_COMPRESSED          0x0001u
#define VE_FLAG_ENCRYPTED           0x0002u
#define VE_FLAG_REVERT_CREATED      0x0004u

// Compression / encryption flags on inode (reserved).
#define GRAHAFS_V2_COMPRESS_NONE    0u
#define GRAHAFS_V2_COMPRESS_LZ4     1u
#define GRAHAFS_V2_COMPRESS_ZSTD    2u
#define GRAHAFS_V2_ENCRYPT_NONE     0u
#define GRAHAFS_V2_ENCRYPT_XTS_AES128 1u

// ---------------------------------------------------------------------------
// On-disk: superblock. 4096 bytes. Lives at LBA 0.
// ---------------------------------------------------------------------------
typedef struct grahafs_v2_superblock {
    uint64_t magic;                     //   0..7    GRAHAFS_V2_SB_MAGIC
    uint32_t version;                   //   8..11   2
    uint32_t block_size;                //  12..15   4096
    uint32_t total_blocks;              //  16..19
    uint32_t bitmap_start_block;        //  20..23
    uint32_t bitmap_blocks;             //  24..27
    uint32_t inode_table_start_block;   //  28..31
    uint32_t inode_table_blocks;        //  32..35
    uint32_t inode_count_max;           //  36..39   16384
    uint32_t data_blocks_start_block;   //  40..43
    uint32_t root_inode;                //  44..47   Usually 1.
    uint64_t segment_table_start;       //  48..55
    uint32_t segment_table_blocks;      //  56..59
    uint32_t segment_count_max;         //  60..63
    uint64_t journal_start_block;       //  64..71
    uint32_t journal_blocks;            //  72..75   16384 (= 64 MB).
    uint32_t _pad0;                     //  76..79
    uint64_t journal_head_block;        //  80..87   Next txn slot.
    uint64_t journal_tail_block;        //  88..95   Oldest still-needed.
    uint32_t free_blocks;               //  96..99
    uint32_t free_inodes;               // 100..103
    uint64_t last_txn_id;               // 104..111  Monotonic per boot.
    uint32_t fs_flags;                  // 112..115
    uint32_t _pad1;                     // 116..119
    char     fs_label[32];              // 120..151  NUL-padded human label.
    uint32_t checksum_sb;               // 152..155  CRC32 of [0..151].
    uint8_t  _pad2[4096 - 156];         // 156..4095
} grahafs_v2_superblock_t;

_Static_assert(sizeof(grahafs_v2_superblock_t) == 4096,
               "grahafs_v2_superblock_t must be exactly 4096 bytes");
_Static_assert(offsetof(grahafs_v2_superblock_t, magic) == 0,                 "sb.magic @ 0");
_Static_assert(offsetof(grahafs_v2_superblock_t, version) == 8,               "sb.version @ 8");
_Static_assert(offsetof(grahafs_v2_superblock_t, journal_start_block) == 64,  "sb.journal_start_block @ 64");
_Static_assert(offsetof(grahafs_v2_superblock_t, checksum_sb) == 152,         "sb.checksum_sb @ 152");

// ---------------------------------------------------------------------------
// On-disk: v2 inode. 512 bytes. Two inodes per block.
// ---------------------------------------------------------------------------
typedef struct grahafs_v2_inode {
    uint32_t magic;                     //   0..3    GRAHAFS_V2_INODE_MAGIC
    uint16_t type;                      //   4..5    1=FILE, 2=DIRECTORY
    uint16_t link_count;                //   6..7
    uint32_t uid;                       //   8..11
    uint32_t gid;                       //  12..15
    uint32_t mode;                      //  16..19
    uint32_t _pad0;                     //  20..23
    uint64_t size;                      //  24..31
    uint64_t blocks_allocated;          //  32..39
    uint64_t creation_time;             //  40..47   ns since boot.
    uint64_t modification_time;         //  48..55
    uint64_t access_time;               //  56..63
    uint32_t direct_blocks[12];         //  64..111
    uint32_t indirect_block;            // 112..115  Covers blocks 12..1035
    uint32_t double_indirect;           // 116..119  Covers blocks 1036..1048575
    uint32_t triple_indirect_reserved;  // 120..123  Reserved; zero in MVP.
    uint32_t _pad1;                     // 124..127
    uint64_t version_chain_head_id;     // 128..135  Newest version_id; 0=empty.
    uint32_t version_chain_segment;     // 136..139  Segment holding head record.
    uint32_t version_count;             // 140..143  1..16.
    uint32_t ai_flags;                  // 144..147  HAS_TAGS|SUMMARY|EMBEDDING|...
    uint32_t ai_importance;             // 148..151
    uint32_t ai_metadata_block;         // 152..155  LBA of extended metadata block.
    uint32_t ai_access_count;           // 156..159
    uint64_t ai_last_modified;          // 160..167
    char     ai_tags[96];               // 168..263  Inline tags, NUL-padded.
    uint64_t ai_embedding[8];           // 264..327  [0]=SimHash; [1..7] reserved.
    uint8_t  ai_reserved[32];           // 328..359  [0..3]=cluster_id; rest reserved.
    uint32_t compression_flags;         // 360..363  Reserved (COMPRESS_*).
    uint32_t encryption_flags;          // 364..367  Reserved (ENCRYPT_*).
    uint32_t checksum_inode;            // 368..371  CRC32 of [0..367].
    uint8_t  _pad2[140];                // 372..511
} grahafs_v2_inode_t;

_Static_assert(sizeof(grahafs_v2_inode_t) == 512,
               "grahafs_v2_inode_t must be exactly 512 bytes");
_Static_assert(offsetof(grahafs_v2_inode_t, magic) == 0,                 "inode.magic @ 0");
_Static_assert(offsetof(grahafs_v2_inode_t, direct_blocks) == 64,        "inode.direct_blocks @ 64");
_Static_assert(offsetof(grahafs_v2_inode_t, indirect_block) == 112,      "inode.indirect_block @ 112");
_Static_assert(offsetof(grahafs_v2_inode_t, double_indirect) == 116,     "inode.double_indirect @ 116");
_Static_assert(offsetof(grahafs_v2_inode_t, version_chain_head_id) == 128, "inode.version_chain_head_id @ 128");
_Static_assert(offsetof(grahafs_v2_inode_t, ai_embedding) == 264,        "inode.ai_embedding @ 264");
_Static_assert(offsetof(grahafs_v2_inode_t, ai_reserved) == 328,         "inode.ai_reserved @ 328");
_Static_assert(offsetof(grahafs_v2_inode_t, checksum_inode) == 368,      "inode.checksum_inode @ 368");

// ---------------------------------------------------------------------------
// On-disk: segment header. 4096 bytes at block 0 of each segment.
// ---------------------------------------------------------------------------
typedef struct grahafs_v2_segment_header {
    uint32_t magic;                     //   0..3    GRAHAFS_V2_SEGMENT_MAGIC
    uint32_t segment_id;                //   4..7    Index into segment table.
    uint64_t creation_txn;              //   8..15   txn_id at allocation.
    uint32_t size_blocks;               //  16..19   Always 32768.
    uint8_t  state;                     //  20       GRAHAFS_V2_SEG_*.
    uint8_t  _pad0[3];                  //  21..23
    uint32_t refcount;                  //  24..27   Version records pointing in.
    uint32_t first_version_offset;      //  28..31   Byte offset in segment of
                                        //           first version_record.
    uint32_t version_record_count;      //  32..35
    uint32_t free_bytes_in_segment;     //  36..39
    uint32_t next_free_block_offset;    //  40..43   For new allocations.
    uint8_t  _pad1[4048];               //  44..4091
    uint32_t checksum_header;           // 4092..4095 CRC32 of [0..4091].
} grahafs_v2_segment_header_t;

_Static_assert(sizeof(grahafs_v2_segment_header_t) == 4096,
               "grahafs_v2_segment_header_t must be 4096 bytes");
_Static_assert(offsetof(grahafs_v2_segment_header_t, state) == 20,             "seg.state @ 20");
_Static_assert(offsetof(grahafs_v2_segment_header_t, refcount) == 24,          "seg.refcount @ 24");
_Static_assert(offsetof(grahafs_v2_segment_header_t, checksum_header) == 4092, "seg.checksum_header @ 4092");

// ---------------------------------------------------------------------------
// On-disk: version record. 128 bytes. Packed into segments.
// ---------------------------------------------------------------------------
typedef struct grahafs_v2_version_record {
    uint32_t magic;                     //   0..3    GRAHAFS_V2_VERSION_MAGIC
    uint32_t _pad0;                     //   4..7
    uint64_t version_id;                //   8..15   Monotonic per file.
    uint64_t timestamp_ns;              //  16..23
    uint64_t size;                      //  24..31   File size at this version.
    uint64_t simhash;                   //  32..39
    uint32_t segment_id;                //  40..43   Same segment as this record
                                        //           (data lives alongside).
    uint32_t cluster_id_at_version;     //  44..47
    uint64_t prev_version;              //  48..55   0 at chain root.
    uint64_t parent_version;            //  56..63   Non-zero iff revert-created.
    uint16_t flags;                     //  64..65   VE_FLAG_*.
    uint16_t _pad1;                     //  66..67
    uint32_t data_block_count;          //  68..71
    uint32_t data_block_index;          //  72..75   Byte offset in segment.
    uint8_t  _pad2[48];                 //  76..123
    uint32_t checksum;                  // 124..127  CRC32 of [0..123].
} grahafs_v2_version_record_t;

_Static_assert(sizeof(grahafs_v2_version_record_t) == 128,
               "grahafs_v2_version_record_t must be 128 bytes");
_Static_assert(offsetof(grahafs_v2_version_record_t, version_id) == 8,      "ver.version_id @ 8");
_Static_assert(offsetof(grahafs_v2_version_record_t, simhash) == 32,        "ver.simhash @ 32");
_Static_assert(offsetof(grahafs_v2_version_record_t, parent_version) == 56, "ver.parent_version @ 56");
_Static_assert(offsetof(grahafs_v2_version_record_t, checksum) == 124,      "ver.checksum @ 124");

// ---------------------------------------------------------------------------
// On-disk: journal transaction begin block.
//
// A transaction occupies a contiguous run of journal blocks:
//   [begin][data_0][data_1]...[data_{D-1}][metadata_0]...[metadata_{M-1}][commit]
// Total blocks = 2 + D + M. begin.block_count stores the total.
//
// block_refs[] is a variable-length array at the end of the begin block;
// the generator/journal code caps txns at what fits in one block (169 refs,
// ≈ 676 KB of payload per txn — enough for a Phase 19 close or mutation).
// ---------------------------------------------------------------------------
typedef struct grahafs_v2_journal_block_ref {
    uint64_t lba_target;                // Main-area LBA the data/meta belongs at.
    uint64_t journal_lba;               // Journal LBA where it's staged.
    uint8_t  kind;                      // 0=data, 1=metadata.
    uint8_t  _pad[7];
} grahafs_v2_journal_block_ref_t;

_Static_assert(sizeof(grahafs_v2_journal_block_ref_t) == 24,
               "journal_block_ref must be 24 bytes");

// Refs array sized so `sizeof(begin_block) <= 4096`. The block starts with
// 48 bytes of scalar header; each ref is 24 bytes; (4096-48)/24 = 168.67 →
// 168 refs gives 48 + 168*24 = 4080 bytes with a 16-byte slack for a
// self-describing trailer.
#define GRAHAFS_V2_JOURNAL_BLOCK_REFS_MAX 168u

#define TXN_FLAG_DATA_FIRST       0x0001u
#define TXN_FLAG_METADATA_ONLY    0x0002u

typedef struct grahafs_v2_journal_begin_block {
    uint32_t magic;                     //   0..3    GRAHAFS_V2_JOURNAL_BEGIN
    uint32_t _pad0;                     //   4..7
    uint64_t txn_id;                    //   8..15
    uint32_t flags;                     //  16..19
    uint32_t _pad1;                     //  20..23
    uint64_t timestamp_ns;              //  24..31
    uint32_t block_count;               //  32..35   Total blocks in this txn
                                        //           including begin and commit.
    uint32_t data_block_count;          //  36..39
    uint32_t metadata_block_count;      //  40..43
    uint32_t ref_count;                 //  44..47
    grahafs_v2_journal_block_ref_t refs[GRAHAFS_V2_JOURNAL_BLOCK_REFS_MAX];
    // Total used: 48 + 168*24 = 4080 bytes. 16-byte trailer pad follows.
    uint8_t  _pad2[16];                 // 4080..4095
} grahafs_v2_journal_begin_block_t;

_Static_assert(sizeof(grahafs_v2_journal_begin_block_t) == 4096,
               "journal begin block must be 4096 bytes");

// Guard: our refs[] formula must not overflow the 4096-byte block.
_Static_assert(48 + GRAHAFS_V2_JOURNAL_BLOCK_REFS_MAX *
               sizeof(grahafs_v2_journal_block_ref_t) <= 4096,
               "journal begin block overflows one disk block");

// ---------------------------------------------------------------------------
// On-disk: journal transaction commit block. Always the last block of the txn.
// ---------------------------------------------------------------------------
typedef struct grahafs_v2_journal_commit_block {
    uint32_t magic;                     //   0..3    GRAHAFS_V2_JOURNAL_COMMIT
    uint32_t _pad0;                     //   4..7
    uint64_t txn_id;                    //   8..15   MUST match begin.txn_id.
    uint32_t checksum;                  //  16..19   CRC32 over
                                        //           [begin+data+metadata].
    uint8_t  _pad1[4076];               //  20..4095
} grahafs_v2_journal_commit_block_t;

_Static_assert(sizeof(grahafs_v2_journal_commit_block_t) == 4096,
               "journal commit block must be 4096 bytes");

